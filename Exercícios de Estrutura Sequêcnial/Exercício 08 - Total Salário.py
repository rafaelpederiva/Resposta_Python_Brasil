''' Faça um Programa que pergunte quanto você ganha por hora e o número de horas trabalhadas no mês. Calcule e mostre
o total do seu salário no referido mês.'''
vHora = int(input('Digite quanto você ganha por hora:'))
vMes = int(input('Digite o número de horas trabalhas no mês:'))
vSalario = vHora*vMes
print('Seu salário é de',vSalario)